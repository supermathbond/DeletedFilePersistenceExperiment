        Configuration _conf;

        public WebsitesLoader()
        {
            try
            {
                if (ConfigurationFileExists())
                {
                    Debug.Write("RandomFileGenerator: Loading configuration.");
                    _conf = Configuration.DeSerialize(AppDomain.CurrentDomain.BaseDirectory);
                }
                else
                {
                    Debug.Write("RandomFileGenerator: First run - creating configuration.");
                    _conf = new Configuration()
                    {
                        ChromeProcessCloseOddsPerSecond = 1000,
                        WebsiteSurfOddsPerSecond = 300
                    };
                    Configuration.Serialize(_conf, AppDomain.CurrentDomain.BaseDirectory);
                }
            }
            catch (Exception ex)
            {
                Debug.Write(ex);
                try
                {
                    Debug.Write(ex);
                }
                catch { }
            }
        }

        public void Run()
        {
            while (true)
            {
                TimeSpan t = DateTime.UtcNow - new DateTime(1970, 1, 1);
                Random r = new Random((int)t.TotalSeconds);

                if (r.Next(_conf.WebsiteSurfOddsPerSecond) == 1)
                {
                    int index = r.Next(_sites.Length);
                    string site = _sites[index];
                    ProcessStartInfo info = new ProcessStartInfo("chrome", site)
                    {
                        UseShellExecute = true
                    };
                    Console.WriteLine("Loading site: " + site);
                    Process.Start(info);
                }

                if (r.Next(_conf.ChromeProcessCloseOddsPerSecond) == 1)
                {
                    Console.WriteLine("Killing Chrome");
                    CloseAllChromeBrowsers();
                }

                 System.Threading.Thread.Sleep(1000);
            }
        }

        private void CloseAllChromeBrowsers()
        {
            Process[] chromeInstances = Process.GetProcessesByName("chrome");

            foreach (Process p in chromeInstances)
                p.Kill();
        }

        private bool ConfigurationFileExists()
        {
            return File.Exists(Path.Combine(AppDomain.CurrentDomain.BaseDirectory, Configuration.FILE_NAME));
        }

